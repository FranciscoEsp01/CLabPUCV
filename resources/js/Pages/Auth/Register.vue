                <TextInput
                    id="email"
                    type="email"
                    class="mt-1 block w-full"
                    v-model="form.email"
                    required
                    autocomplete="username"
                    placeholder="tu.nombre@mail.pucv.cl"
                    pattern=".*@mail\.pucv\.cl$"
                    title="Debe ser un correo institucional (@mail.pucv.cl)"
                />

